package code_editor;

public class Editor_SelectedTile {

	private int x, y;
	
	public Editor_SelectedTile(int x, int y){
		
		this.x = x;
		this.y = y;
		
	}

	public int getX() {
		
		return x;
		
	}

	public void setX(int x) {
		
		this.x = x;
		
	}

	public int getY() {
		
		return y;
		
	}

	public void setY(int y) {
		
		this.y = y;
		
	}
	
}
